public class Score2{

    public static void main(String args[]) {
        int i=99;
    if (i >= 90) {
        System.out.println("成绩" + i + "分是A等级");
    }
    else if (i >= 80) {
        System.out.println("成绩" + i + "分是B等级");
    }
    else if (i >= 70) {
        System.out.println("成绩" + i + "分是C等级");
    }
    else if (i >= 60) {
        System.out.println("成绩" + i + "分是D等级");
    }
    else {
        System.out.println("成绩" + i + "分是D等级");
    }
    }
}
